package demoqa1.com.model;

public class DataDemoqa {
    public String strBlack;
    public String strWhite;

    public DataDemoqa(String strBlack, String strWhite) {
        this.strBlack = strBlack;
        this.strWhite = strWhite;
    }

    public String getStrBlack() {
        return strBlack;
    }

    public void setStrBlack(String strBlack) {
        this.strBlack = strBlack;
    }

    public String getStrWhite() {
        return strWhite;
    }

    public void setStrWhite(String strWhite) {
        this.strWhite = strWhite;
    }
}
